const pictureDistractors = ["☀️", "🏖️", "📱", "🎁", "🚆", "💧", "🥐", "😊", "😴", "🚗", "🎒", "🏫", "🛒", "🔑", "✈️"];

function makeQuestion(question: string, answer: string, options: string[]): QuizQuestion {
  return { question, answer, options };
}

function createStory(spec: StorySpec): Story {
  const vocabulary = spec.vocabulary.slice(0, 15);
  const firstWord = vocabulary[0];
  const secondWord = vocabulary[1] ?? vocabulary[0];
  const thirdWord = vocabulary[2] ?? vocabulary[0];
  const fourthWord = vocabulary[3] ?? vocabulary[0];
  const fifthWord = vocabulary[4] ?? vocabulary[0];

  return {
    ...spec,
    text: [],
    vocabulary,
    challenges: [
      {
        id: `${spec.id}-translation`,
        type: "translation",
        prompt: "Choose the translation.",
        word: firstWord.word,
        options: [firstWord.translation, ...distractors.filter((item) => item !== firstWord.translation).slice(0, 3)],
        answer: firstWord.translation,
      },
      {
        id: `${spec.id}-picture`,
        type: "picture",
        prompt: "Choose the picture.",
        word: secondWord.word,
        options: [secondWord.pictureLabel ?? "💬", ...pictureDistractors.filter((item) => item !== secondWord.pictureLabel).slice(0, 3)],
        answer: secondWord.pictureLabel ?? "💬",
      },
      {
        id: `${spec.id}-fill`,
        type: "fill",
        prompt: "Fill the gap.",
        sentence: thirdWord.example.replace(thirdWord.word, "____"),
        options: [thirdWord.word, "morning", "station"],
        answer: thirdWord.word,
      },
      {
        id: `${spec.id}-match`,
        type: "match",
        prompt: "Match the word.",
        word: fourthWord.word,
        options: [fourthWord.translation, ...distractors.filter((item) => item !== fourthWord.translation).slice(3, 6)],
        answer: fourthWord.translation,
      },
      {
        id: `${spec.id}-true-false`,
        type: "trueFalse",
        prompt: "True or false?",
        statement: `${fifthWord.word} means ${fifthWord.translation}.`,
        answer: "True",
      },
    ],
    quiz: spec.quiz ?? buildFinalQuiz(spec, vocabulary),
  };
}

